package org.eclipse.scout.rt.client.ui.basic.table;

/**
 * Defines the display position of the aggregation rows in a table.
 */
public enum GroupingStyle {

  /**
   * Aggregation rows in table are displayed on top of a row group.
   */
  TOP,

  /**
   * Aggregation rows in table are displayed on the bottom of a row group (default).
   */
  BOTTOM

}
